rev = int(input("Введите значение выручки компании: "));
costs = int(input("Введите значение издержек компании: "));
if rev > costs:
    print("Данная фирма работает в прибыль");
    rent = (rev / costs);
    kol_sotr = int(input("Ввдеите количество сотрудников компании: "));
    rent1 = rent / kol_sotr;
    print("Прибыль в расчете на однго сотрудника составляет: ", rent1);
else:
    print("Данная фирма работает в убыток");