a = int(input("Введите, сколько киломаетров вы пробежали в первый день: "));
b = int(input("Введите цель, сколько километров вы хотите пробежать: "));
n = 1;
while a < b:
    a = a + a / 10;
    n = n + 1;
    print("В ", n, "-й день: ", '%.2f' % a, "км");
print("На ", n, "-й день спортсмен достиг результата — не менее", b, "км");
